<?php

namespace App;

use App\QueryFilter\Id;
use Illuminate\Database\Eloquent\Model;
use Illuminate\Pipeline\Pipeline;

class Course extends Model implements Imagable
{
    protected $hidden = array('pivot');
    public $timestamps = false;
    protected $guarded = [];

    public static function allCourses($center)
    {
        return app(Pipeline::class)
            ->send($center->courses())
            ->through([
                Id::class
            ])
            ->thenReturn()
            ->get();
    }


    public function center(){
        return $this->belongsTo(Center::class);
    }

    public function instructors(){
        return $this->belongsToMany(Instructor::class);
    }

    public function groups()
    {
        return $this->hasMany(CourseGroup::class);
    }

    public function images()
    {
        return $this->morphMany(Image::class,'imageable');
    }

    public function categories(){
        return $this->belongsToMany(Category::class);
    }


    protected static function boot()
    {
        parent::boot(); // TODO: Change the autogenerated stub
        static::deleting(function($course){
            if($course->images) $course->images()->delete();
        });
    }


    public function uploadImages($images)
    {
        foreach ($images as $image) {
            $original = $this->saveImage($image);
//            echo $original;
            $this->images()->create([
                'url' => url("/uploads/courses/".$original)
            ]);
        }
    }

    public function saveImage($image)
    {
        return Image::saveImage($this->getDir(), $image);
    }

    public function deleteImage($image)
    {
        // TODO: Implement deleteImage() method.
    }

    public function getDir()
    {
        return '/uploads/courses';
    }
}
